def add(n1, n2):
    return n1 + n2


def sub(n1, n2):
    return n1 - n2


def mul(n1, n2):
    return n1 * n2


def div(n1, n2):
    return n1 / n2


operations = {"+": add, "-": sub, "*": mul, "/": div}


def calculator():
    print(logo)
    num1 = float(input("Enter the first number:\n"))
    should_continue = True
    while should_continue:
        for symbols in operations:
            print(symbols)
        opr = input("Choose the operation you want to perform:\n")
        num2 = float(input("Enter the another number:\n"))
        calculation_fuction = operations[opr]
        answer = calculation_fuction(num1, num2)
        print(f"{num1} {opr} {num2} = {answer}")
        choice1 = input(
            "Do you want to continue performing operation using the answer?Type Yes or No:"
        ).lower()
        if choice1 == "yes":
            num1 = answer
        else:
            should_continue = False
            calculator()


calculator()
